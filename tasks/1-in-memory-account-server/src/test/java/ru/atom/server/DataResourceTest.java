package ru.atom.server;

import org.junit.Assert;
import org.junit.Test;

import javax.ws.rs.core.Response;

/**
 * Created by Ксения on 28.03.2017.
 */
public class DataResourceTest {
    @Test
    public void users() throws Exception {

        Response response = DataResource.users();
        Assert.assertTrue(response.getStatus() == 200
                || response.getEntity().equals("{ \"users\" : []}"));

        AuthResource.register("Sara", "OPX");
        AuthResource.login("Sara", "OPX");
        response = DataResource.users();
        Assert.assertTrue(response.getStatus() == 200);
        AuthResource.register("Max", "bbX");
        AuthResource.login("Max", "bbX");
        response = DataResource.users();
        Assert.assertTrue(response.getStatus() == 200);

    }


}